#!/usr/bin/env node
/**
 * beforeShellExecution — consolidated shell policy for afenda-Xerp.
 */
import {
  allow,
  ask,
  deny,
  extractCommand,
  hasDrizzleWorkflow,
  hasEnvSyncWorkflow,
  log,
  parseStdinJson,
  resolveRepoRoot,
} from "./_hook-utils.mjs";

const TAG = "guard-shell-policy";

const DESTRUCTIVE_GIT = /\bgit\b[\s\S]*\b(restore|checkout\s+--|clean)\b/i;
const SAFE_COMMAND =
  /\b(typecheck|lint|check:|test:|vitest|jest|knip|scorecard)\b/i;
const DEV_OR_START =
  /\b(pnpm|npm|yarn|bun|npx)\b[\s\S]*\b(dev|start)\b|\bnext\s+dev\b|\bstorybook\s+dev\b/i;
const WRONG_INSTALL =
  /\b(npm\s+(install|i)\b|yarn\s+(add|install)\b|bun\s+(add|install)\b)/i;
const PRODUCTION_DEPLOY = /\bvercel\b[\s\S]*(?:--prod(?:uction)?|\bpromote\b)/i;
const SAFE_VERCEL =
  /\bvercel\b[\s\S]*\b(env\s+(pull|ls|list|add|rm|remove)|link|login|logout|whoami|inspect|logs|dev)\b/i;
const ENV_PULL = /\bvercel\b[\s\S]*\benv\s+pull\b/i;
const SHELL_WRITE_HINT =
  /(?:>>?|\|\s*tee\b|\bSet-Content\b|\bAdd-Content\b|\bOut-File\b|\bsp\s)/i;

const SYNCED_ENV_RELATIVE = [
  ".env",
  ".env.local",
  "apps/erp/.env.local",
  "apps/docs/.env.local",
  "packages/database/.env",
];

const normalizeCommand = (command) =>
  command.trim().replace(/\s+/g, " ").replace(/\\\\/g, "/");

function checkDestructiveGit(command) {
  if (!(command && DESTRUCTIVE_GIT.test(command))) {
    return null;
  }

  if (/\b(-n|--dry-run|--help|-h)\b/.test(command)) {
    return null;
  }

  return {
    user_message:
      "This git command can discard uncommitted or untracked work. Confirm only if you explicitly want that.",
    agent_message:
      "Destructive git (restore / checkout -- / clean) requires user approval. Do not retry unless the user explicitly requested discarding WIP.",
  };
}

function checkWrongPackageManager(command) {
  if (!(command && WRONG_INSTALL.test(command))) {
    return null;
  }

  return {
    user_message:
      "This repo uses pnpm (see packageManager in package.json). Confirm if you intentionally want npm/yarn/bun here.",
    agent_message:
      "Prefer pnpm install / pnpm add / pnpm --filter <pkg> <script>. Retry with pnpm unless the user explicitly chose another package manager.",
  };
}

function checkDevServers(command) {
  if (!command || SAFE_COMMAND.test(command) || !DEV_OR_START.test(command)) {
    return null;
  }

  return {
    user_message:
      "This command may start a long-running dev/start server. Confirm only if you want a new server process.",
    agent_message:
      "Dev/start server blocked pending approval. The user may already have apps/erp on port 3000 or apps/docs on 3001. Prefer typecheck, pnpm check, or test unless they explicitly asked to start dev.",
  };
}

function checkVercelProduction(command) {
  if (
    !command ||
    SAFE_VERCEL.test(command) ||
    !PRODUCTION_DEPLOY.test(command)
  ) {
    return null;
  }

  return {
    user_message:
      "This command may deploy or promote to Vercel production. Confirm only if intentional.",
    agent_message:
      "Production Vercel action blocked pending approval. Run pnpm check and build locally first. Ensure production env vars and database targets are correct.",
  };
}

function checkVercelEnvPull(command) {
  if (!(command && ENV_PULL.test(command))) {
    return null;
  }

  return {
    user_message:
      "`vercel env pull` may overwrite local .env files. Confirm if you want to sync from Vercel.",
    agent_message:
      "vercel env pull blocked pending approval. Never commit pulled secrets.",
  };
}

function isAllowedShellCommand(command) {
  const normalized = command.trim().replace(/\s+/g, " ");

  return (
    /pnpm\s+(env:sync|env:doctor)\b/.test(normalized) ||
    /sync-env\.mjs\b/.test(normalized)
  );
}

function checkSyncedEnvShellWrite(command, repoRoot) {
  if (
    !(hasEnvSyncWorkflow(repoRoot) && command) ||
    isAllowedShellCommand(command)
  ) {
    return null;
  }

  if (!SHELL_WRITE_HINT.test(command)) {
    return null;
  }

  for (const relativePath of SYNCED_ENV_RELATIVE) {
    const pathPattern = relativePath
      .replace(/\./g, "\\.")
      .replace(/\//g, "[/\\\\]");
    if (new RegExp(pathPattern, "i").test(command)) {
      return {
        kind: "deny",
        user_message:
          "This env file is generated by pnpm env:sync. Edit .env.config or .env.secret, then run pnpm env:sync.",
        agent_message:
          "Blocked direct shell write to synced env output. Edit .env.config or .env.secret, then run pnpm env:sync.",
      };
    }
  }

  return null;
}

function isReadOnlyMigrationAudit(command) {
  return /packages[/\\]database[/\\]scripts[/\\](compare-migrations|db-migration-status|list-migration-hashes)\.mjs/.test(
    command
  );
}

function isAllowedRepairJournal(command) {
  return (
    /pnpm\s+--filter\s+@afenda\/database\s+db:repair-journal(:check)?(\s|$)/.test(
      command
    ) ||
    (/repair-drizzle-journal\.mjs/.test(command) &&
      !/\bdrizzle-kit\s+migrate\b/.test(command))
  );
}

function isAllowedShellMigration(command) {
  const normalized = normalizeCommand(command);

  if (/^pnpm(\s+run\s+)?migrate(:deploy)?(\s|$)/.test(normalized)) {
    return true;
  }

  if (
    /pnpm\s+--filter\s+@afenda\/database\s+db:(migrate|generate)(\s|$)/.test(
      normalized
    )
  ) {
    return true;
  }

  if (isAllowedRepairJournal(normalized)) {
    return true;
  }

  return isReadOnlyMigrationAudit(normalized);
}

function checkBlockedMigration(command, repoRoot) {
  if (!(hasDrizzleWorkflow(repoRoot) && command)) {
    return null;
  }

  const normalized = normalizeCommand(command);

  if (isAllowedShellMigration(normalized)) {
    return null;
  }

  const blocked =
    /\bdrizzle-kit\s+migrate\b/.test(normalized) ||
    /\bdrizzle-kit\s+push\b/.test(normalized) ||
    /\bapply-orbit-migrations\.mjs\b/.test(normalized) ||
    /\bpsql\b[\s\S]*packages[/\\]database[/\\]drizzle[/\\][\w-]+\.sql/.test(
      normalized
    ) ||
    /\bnode\b[\s\S]*packages[/\\]database[/\\]drizzle[/\\][\w-]+\.sql/.test(
      normalized
    ) ||
    (/\bnode\b[\s\S]*packages[/\\]database[/\\]scripts[/\\][\w-]+\.mjs/.test(
      normalized
    ) &&
      /packages[/\\]database[/\\]drizzle[/\\]00\d{2}_/.test(normalized)) ||
    /\b(pool\.query|execute_sql|\.query\s*\()\b[\s\S]*packages[/\\]database[/\\]drizzle/.test(
      normalized
    );

  if (!blocked) {
    return null;
  }

  return {
    kind: "deny",
    user_message:
      "Database schema changes must use the repo Drizzle workflow: pnpm migrate or pnpm --filter @afenda/database db:migrate.",
    agent_message:
      "Blocked non-Drizzle migration path. Use pnpm migrate or pnpm --filter @afenda/database db:migrate instead of manual SQL or direct drizzle-kit migrate.",
  };
}

const input = parseStdinJson();

if (input === null) {
  log(TAG, "invalid stdin JSON; allowing");
  allow();
}

const repoRoot = resolveRepoRoot();
const command = extractCommand(input);

const checks = [
  checkDestructiveGit,
  checkWrongPackageManager,
  checkDevServers,
  checkVercelProduction,
  checkVercelEnvPull,
  (value) => checkSyncedEnvShellWrite(value, repoRoot),
  (value) => checkBlockedMigration(value, repoRoot),
];

for (const check of checks) {
  const result = check(command);

  if (!result) {
    continue;
  }

  log(TAG, `matched: ${check.name}`);

  if (result.kind === "deny") {
    deny(result.user_message, result.agent_message);
  }

  ask(result.user_message, result.agent_message);
}

allow();
