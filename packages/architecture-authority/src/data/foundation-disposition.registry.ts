export const foundationDispositionRegistry = {
  fingerprint: FOUNDATION_DISPOSITION_FINGERPRINT,
  entries: foundationDispositionEntries,
} satisfies FoundationDispositionRegistry;

export const FOUNDATION_DISPOSITION_REGISTRY =
  foundationDispositionRegistry.entries;

const foundationDispositionById = createReadonlyLookupMap<
  string,
  FoundationDispositionEntry
>(
  FOUNDATION_DISPOSITION_REGISTRY.map(
    (entry): readonly [string, FoundationDispositionEntry] => [entry.id, entry]
  )
);

export function getFoundationDispositionEntry(
  id: string
): FoundationDispositionEntry | undefined {
  return foundationDispositionById.get(id);
}

export function getFoundationDispositionEntriesByLane(
  lane: FoundationDispositionEntry["lane"]
): readonly FoundationDispositionEntry[] {
  return FOUNDATION_DISPOSITION_REGISTRY.filter((entry) => entry.lane === lane);
}
